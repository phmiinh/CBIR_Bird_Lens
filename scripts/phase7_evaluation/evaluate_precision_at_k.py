from __future__ import annotations

import argparse
import sys
from pathlib import Path

SHARED_DIR = Path(__file__).resolve().parents[1] / "shared"
if str(SHARED_DIR) not in sys.path:
    sys.path.append(str(SHARED_DIR))

from feature_utils import load_json


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Compatibility wrapper. Use evaluate_retrieval.py for the DB-first evaluation pipeline."
    )
    parser.add_argument(
        "--output-json",
        default="outputs/eval/species_proxy_summary.json",
        help="Path to the species_proxy summary JSON generated by evaluate_retrieval.py.",
    )
    return parser.parse_args()


def main() -> int:
    summary_path = Path(parse_args().output_json).resolve()
    if not summary_path.exists():
        raise FileNotFoundError(
            f"{summary_path} was not found. Run scripts/phase7_evaluation/evaluate_retrieval.py first."
        )
    payload = load_json(summary_path)
    print("Deprecated entrypoint. Read the DB-first summary JSON below:")
    print(payload)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
